public class menuEntity {
    //菜单实体类
    private int id;
    private String wname;
    private String whtml;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getWname() {
        return wname;
    }

    public void setWname(String wname) {
        this.wname = wname;
    }

    public String getWhtml() {
        return whtml;
    }

    public void setWhtml(String whtml) {
        this.whtml = whtml;
    }
}
